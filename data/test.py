import requests

# Замените на ID работы, которую хотите удалить (если она существует)
JOB_ID_TO_DELETE = 1  # <--- ВАЖНО: Измените это значение!

# Базовый URL API (укажите адрес вашего Flask-приложения)
API_BASE_URL = "http://127.0.0.1:5000/api/jobs"

# URL для удаления работы (комбинируем базовый URL и ID работы)
DELETE_URL = f"{API_BASE_URL}/{JOB_ID_TO_DELETE}"

print(f"Отправляем DELETE запрос на URL: {DELETE_URL}")

# Отправляем DELETE-запрос
response = requests.delete(DELETE_URL)

print(f"Получен статус код: {response.status_code}")

# Проверяем статус код ответа
if response.status_code == 204:
    print("Удаление работы прошло успешно.  Сервер вернул код 204 No Content.")
    print("Это означает, что работа с указанным ID была успешно удалена.")
elif response.status_code == 404:
    print("Ошибка: Работа с указанным ID не найдена (код 404 Not Found).")
    print("Возможно, работа с таким ID не существует в базе данных.")
else:
    print(f"Произошла неожиданная ошибка.  Статус код: {response.status_code}")
    print("Пожалуйста, проверьте Flask-приложение на наличие ошибок.")

print("\n--- Важно ---")
print("1. Убедитесь, что Flask-приложение запущено.")
print("2. Убедитесь, что работа с указанным JOB_ID_TO_DELETE существует в базе данных.")
print("3. Если вы видите ошибку, проверьте логи Flask-приложения для получения дополнительной информации.")
